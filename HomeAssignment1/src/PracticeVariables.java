public class PracticeVariables {
    public static void main(String[] args) {

        byte b1=-12,b2=24,b3=0;
        short s1=234,s2=-120,s3=4556;
        int i1=25 + 2 * (1 + 4) / 5 % 1,i2=22,i3=134566;
        long l1=23456L,l2=-32456L,l3=2121321432341L;
        float f1=3.14F,f2=-3.14F,f3=2.56F;
        double d1=3.134,d2=2.456,d3=5.456;
        boolean bool1=false,bool2=true;
        char c1='h',c2='9',c3='n';

        System.out.println(i1);

        System.out.println("Bytes: "+b1+", "+b2+", "+b3);
        System.out.println("Shorts: "+s1+", "+s2+", "+s3);
        System.out.println("Ints: "+i1+", "+i2+", "+i3);
        System.out.println("Longs: "+l1+", "+l2+", "+l3);
        System.out.println("Floats: "+f1+", "+f2+", "+f3);
        System.out.println("Doubles: "+d1+", "+d2+", "+d3);
        System.out.println("Booleans: "+bool1+", "+bool2);
        System.out.println("Chars: "+c1+", "+c2+", "+c3);


    }
}
