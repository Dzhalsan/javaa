package Day3;

public class ManipulationVariable {

    public static void main(String[] args) {
        double x = 7.5;
        double y = 2*x;
        double z = ((3*y)+x);
        double p = y/3;
        System.out.println("x = "+x);
        System.out.println("y = "+y);
        System.out.println("z = "+z);
        System.out.println("p = "+p);
    }
}
